void solve() {
    string s; cin >> s;
    vector<int> v(26);
    for (char x : s) {
        v[x - 'a']++;
    }

    auto can = [&](int x) -> bool {
        vector<int> cur = v;
        int need = x;
        for (int i = 0; i < 26; i++) {
            int k = min(need, cur[i] / 2);
            need -= k;
            cur[i] -= 2 * k;
        }
        if (need > 0) return false;
        int sum = accumulate(cur.begin(), cur.end(), 0);
        return (sum >= x);
    };

    int n = s.length();
    int l = 0, r = n, ans = 0;
    while (l <= r) {
        int mid = (l + r) >> 1;
        if (can(mid)) {
            l = mid + 1;
            ans = mid;
        } else {
            r = mid - 1;
        }
    }
    cout << ans << '\n';
}

