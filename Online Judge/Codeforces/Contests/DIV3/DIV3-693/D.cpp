void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.rbegin(), a.rend());
    long long odd = 0, even = 0;
    for (int i = 0; i < n; i++) {
        if (i % 2 == 0) {
            if (a[i] % 2 == 0) even += a[i];
        } else {
            if (a[i] % 2 == 1) odd += a[i];
        }
    }
    if (even > odd) {
        cout << "Alice" << '\n';
    } else if (even == odd) {
        cout << "Tie" << '\n';
    } else {
        cout << "Bob" << '\n';
    }
}

