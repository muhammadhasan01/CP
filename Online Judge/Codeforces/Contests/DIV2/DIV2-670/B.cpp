const int N = 1e5 + 5;
const long long INF = 1e18;

int tc;
int n;
long long a[N];

void solve() {
    cin >> n;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    sort(a + 1, a + 1 + n);
    long long ans = -INF;
    for (int x = 0; x <= 5; x++) {
        int y = 5 - x;
        long long res = 1;
        for (int i = 1; i <= x; i++)
            res *= a[i];
        for (int j = n; j >= n - y + 1; j--)
            res *= a[j];
        debug(x, res);
        ans = max(ans, res);
    }
    cout << ans << '\n';
}

int main() { 
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    cin >> tc;
    for (int T = 1; T <= tc; T++) {
        solve();
    }

    return 0;
}