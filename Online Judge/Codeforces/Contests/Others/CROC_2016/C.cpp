const int N = 1e5 + 5;

int n, k;
char s[N];
int pre[N];

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    cin >> n >> k;
    for (int i = 1; i <= n; i++) {
        cin >> s[i];
        pre[i] = pre[i - 1];
        if (s[i] == '0') pre[i]++;
    }

    auto can = [&](int x) -> bool {
        for (int i = 1; i <= n; i++) {
            if (s[i] == '1') continue;
            int L = max(1, i - x);
            int R = min(n, i + x);
            int total = pre[R] - pre[L - 1];
            if (total >= k + 1) return true;
        }
        return false;
    };

    int l = 1, r = n, ans = n;
    while (l <= r) {
        int mid = (l + r) >> 1;
        if (can(mid)) {
            r = mid - 1;
            ans = mid;
        } else {
            l = mid + 1;
        }
    }
    cout << ans << '\n';

    return 0;
}