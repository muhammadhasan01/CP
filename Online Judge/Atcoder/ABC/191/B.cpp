const int N = 1e5 + 5;

int n, x;
int a[N];

void solve() {
    cin >> n >> x;
    vector<int> res;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        if (a[i] == x) continue;
        res.emplace_back(a[i]);
    }
    int len = res.size();
    for (int i = 0; i < len; i++) {
        cout << res[i] << " \n"[i == len - 1];
    }
}

